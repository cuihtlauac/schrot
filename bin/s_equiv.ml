(* Count S-equivalence classes on guillotine rectangulations.
   S-equivalence = generated by simple flips + wall slides.
   Compare with D4-equivalence (geometric symmetry). *)

let label_tiling (is_h, tree) =
  let c = ref 0 in
  let rec go = function
    | Schrot.Tile () -> let n = !c in incr c; Schrot.Tile n
    | Schrot.Frame ch -> Schrot.Frame (List2.map (fun (w, c) -> (w, go c)) ch)
  in (is_h, go tree)

(* Union-Find on string keys *)
module UF = struct
  let parent = Hashtbl.create 256
  let rank = Hashtbl.create 256

  let clear () = Hashtbl.clear parent; Hashtbl.clear rank

  let find x =
    let rec go x =
      match Hashtbl.find_opt parent x with
      | None -> Hashtbl.replace parent x x; Hashtbl.replace rank x 0; x
      | Some p -> if p = x then x else let r = go p in Hashtbl.replace parent x r; r
    in go x

  let union x y =
    let rx = find x and ry = find y in
    if rx <> ry then begin
      let rk_x = Hashtbl.find rank rx and rk_y = Hashtbl.find rank ry in
      if rk_x < rk_y then Hashtbl.replace parent rx ry
      else if rk_x > rk_y then Hashtbl.replace parent ry rx
      else begin Hashtbl.replace parent ry rx; Hashtbl.replace rank rx (rk_x + 1) end
    end

  let count_classes keys =
    let roots = Hashtbl.create 64 in
    List.iter (fun k -> Hashtbl.replace roots (find k) ()) keys;
    Hashtbl.length roots
end

let () =
  let max_n = ref 8 in
  Arg.parse [
    "--max-leaves", Arg.Set_int max_n, "N Max leaves (default: 8)";
  ] (fun _ -> ()) "s_equiv [--max-leaves N]";
  Printf.printf "  n  total  S-equiv  D4-equiv  S∧D4\n";
  Printf.printf "  -  -----  -------  --------  ----\n";
  for n = 1 to !max_n do
    UF.clear ();
    let tilings = List.map label_tiling (Schrot.enum n) in
    let keys = List.map Tiling.to_string tilings in
    (* Initialize UF *)
    List.iter (fun k -> ignore (UF.find k)) keys;
    (* Build S-equivalence: simple flips + wall slides *)
    List.iter (fun t ->
      let t_key = Tiling.to_string t in
      let flips = Tiling.enumerate_flips t in
      List.iter (fun (flip, t') ->
        match flip with
        | Tiling.Simple_dissolve _ | Tiling.Simple_create _
        | Tiling.Wall_slide _ ->
          UF.union t_key (Tiling.to_string t')
        | Tiling.Pivot_out _ | Tiling.Pivot_in _ | Tiling.T_flip _ -> ()
      ) flips
    ) tilings;
    let s_classes = UF.count_classes keys in
    (* D4-equivalence *)
    let d4_tbl = Hashtbl.create 64 in
    List.iter (fun t ->
      Hashtbl.replace d4_tbl (Tiling.canonical_d4 t) ()
    ) tilings;
    let d4_classes = Hashtbl.length d4_tbl in
    (* S ∧ D4: meet = finest equivalence coarser than both *)
    (* = union of S-edges and D4-edges *)
    UF.clear ();
    List.iter (fun k -> ignore (UF.find k)) keys;
    (* S-edges *)
    List.iter (fun t ->
      let t_key = Tiling.to_string t in
      let flips = Tiling.enumerate_flips t in
      List.iter (fun (flip, t') ->
        match flip with
        | Tiling.Simple_dissolve _ | Tiling.Simple_create _
        | Tiling.Wall_slide _ ->
          UF.union t_key (Tiling.to_string t')
        | Tiling.Pivot_out _ | Tiling.Pivot_in _ | Tiling.T_flip _ -> ()
      ) flips
    ) tilings;
    (* D4-edges *)
    List.iter (fun t ->
      let t_key = Tiling.to_string t in
      let orbit = Tiling.d4_orbit t in
      List.iter (fun t' ->
        let t'_rel = Tiling.relabel t' in
        UF.union t_key (Tiling.to_string t'_rel)
      ) orbit
    ) tilings;
    let meet_classes = UF.count_classes keys in
    Printf.printf "%3d  %5d  %7d  %8d  %4d\n"
      n (List.length tilings) s_classes d4_classes meet_classes;
    flush stdout
  done
